def GetNucleobase(atomnames):
    ## using standard amber (OL15/OL3) atomnames, 
    ## we can find the specific nucleobase by a 
    ## single atomname's presence.
    if "O2'" in atomnames:
        # RNA found
        if "H61" in atomnames:
            return "rA"
        if "H41" in atomnames:
            return "rC"
        if "H22" in atomnames:
            return "rG"
        if "H3" in atomnames:
            return "rU"
    else:
        # RNA not found, assuming DNA
        if "H61" in atomnames:
            return "dA"
        if "H5" in atomnames:
            return "dC"
        if "H22" in atomnames:
            return "dG"
        if "H3" in atomnames:
            return "dT"

